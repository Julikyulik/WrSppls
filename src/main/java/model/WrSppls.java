package model;

public class WrSppls {

    public Integer serial_number;
    public String color;
    public Double thickness_track;
    public Double line_length;

    public Integer getSerial_number() {
        return serial_number;
    }

    public void setSerial_number(Integer serial_number) {
        this.serial_number = serial_number;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public Double getThickness_track() {
        return thickness_track;
    }

    public void setThickness_track(Double thickness_track) {
        this.thickness_track = thickness_track;
    }

    public Double getLine_length(){
        return line_length;
    }

    public void setLine_length(Double line_length) {
        this.line_length = line_length;
    }

}
